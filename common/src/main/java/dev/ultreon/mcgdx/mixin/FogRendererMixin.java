package dev.ultreon.mcgdx.mixin;

import dev.ultreon.mcgdx.GdxMinecraft;
import net.minecraft.client.renderer.FogRenderer;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.Shadow;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

@Mixin(FogRenderer.class)
public class FogRendererMixin {
    @Shadow private static float fogRed;

    @Shadow private static float fogGreen;

    @Shadow private static float fogBlue;

    @Inject(method = "Lnet/minecraft/client/renderer/FogRenderer;levelFogColor()V", at = @At("HEAD"))
    private static void aaaa(CallbackInfo ci) {
        GdxMinecraft.fogColor.set(fogRed, fogGreen, fogBlue, 1);
    }
}
